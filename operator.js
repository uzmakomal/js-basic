//Basic Arithimatic operators:

let a=10;
let b=4;
console.log("a+b= ",a+b);
console.log("a-b= ",a-b);
console.log("a*b= ",a*b);
console.log("a/b= ",a/b);
console.log("a**b= ",a**b);//exponentiation operator
console.log("a%b= ",a%b);// reminder operator
console.log("a++=",a++);//increment operator
console.log("a=",a);
console.log("b--=",b--);//decrement operator
console.log("b=",b);
// Assigment Operator:
